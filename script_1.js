// *******************************
function mySeconds(a, b, c) {
  let t = new Date();
  t.setHours(a, b, c);
  let h = t.getHours();
  let m = t.getMinutes();
  let s = t.getSeconds();
  let result = h + ":" + m + ":" + s;
  let seconds = h * 3600 + m * 60 + s;
  return seconds;
  //   console.log(seconds);
}
mySeconds(1, 1, 5);
