// Написать функцию, которая принимает количество секунд, переводит их в часы, минуты и секунды и возвращает в виде строки «чч:мм:сс».
// *******************************
function strTime(stm) {
  let t = new Date(stm * 1000 - 3600000 * 3);
  let h = t.getHours();
  let m = t.getMinutes();
  let s = t.getSeconds();
  //   let result = h + ":" + m + ":" + s;
  let result = `${h}:${m}:${s}`;
  return result;
  //   console.log(result);
}
